package POO;
public class Calculadora {

    public int dobrar(int numero) {
        return numero * 2;
    }

    public int triplicar(int numero) {
        return numero * 3;
    }

    public int somar(int a, int b) {
        return a + b;
    }

    public int subtrair(int a, int b) {
        return a - b;
    }
}

